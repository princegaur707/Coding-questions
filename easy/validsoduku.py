class Solution:
    def isValidSudoku(self, board):
        for i in range(9):
            for j in range(9):
                if i!=j:
                    if board[i][i]==board[i][j] or board[i][i]==board[j][i]:
                        if board[i][j].isdigit():
                            return False
                    
        return True
