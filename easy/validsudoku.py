class Solution:
    def isValidSudoku(self, board):
        values=[]
        for row,row_values in enumerate(board):
            for column,value in enumerate(row_values):
                if value!=".":
                    values+=[(row,value),(value,column),(row//3,column//3,value)]
                    #print(values)
        return len(values)==len(set(values))
